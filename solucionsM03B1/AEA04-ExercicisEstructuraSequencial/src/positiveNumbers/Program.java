public class Program {

    /**
     * Verifies if a real number is a positive number.
     * 
     * @param a a real number
     * @return true if -a- is a positive number, false otherwise
     */
    public boolean isPositive(double a) {
        boolean isAPositiveNumber = a > 0;
        return isAPositiveNumber;
    }
}
